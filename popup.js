const header = $("header");
const textbox = $("#textbox");
const left_arrow = $("#arrow-buttons button").eq(0);
const right_arrow = $("#arrow-buttons button").eq(1);
const clear_button = $("#clear-button");
const copy_button = $("#copy-button");

var fonts_array = [bold_map, italic_map, bold_italic_map, serif_map, serif_italic_map, sans_map, bold_sans_map, monospace_map, double_struck_map, small_caps_map, cursive_map, fraktur_map, fraktur_bold_map, squares_map, circle_map, filled_circle_map, wide_map, superscript_map, strikethrough_map, underlined_map];
var fonts_name_array = ["Bold", "Italic", "Bold Italic", "Serif", "Serif Italic", "Sans", "Bold Sans", "MonoSpace", "Double Struck", "Small Caps", "Cursive", "Fraktur", "Fraktur Bold", "Squares", "Circles", "Filled Circles", "Wide", "Subscript", "Superscript", "StrikeThrough", "Underlined"];
var current_font = 0;

var textbox_blured = false;


function changeHeader() {
    header.text("Font: " + fonts_name_array[current_font]);
}


function applyFont(letter_map, text) {
    if (!text) {
        return false;
    }

    let font_string = "";
    for (let letter of text) {
        if (letter_map[letter]) {
            font_string += letter_map[letter];
        } else font_string += letter
    }

    return font_string;
}


// Makes it where you can start typing on the textbox as soon as the extension is opened
textbox.focus();


// Font converter
textbox.on('input', function() {
    let current_text = textbox.val();
    console.log(current_text);
    let font = fonts_array[current_font]
    let font_text = applyFont(font, current_text);

    if (font_text == false) {
        return;
    } 

    let cursor_positon = textbox[0].selectionStart;

    textbox.val(font_text);
    localStorage.setItem("savedText", current_text);

    // Prevents cursor from jumping to the end everytime
    // so you can type in the middle of the text
    textbox[0].selectionStart = cursor_positon;
    textbox[0].selectionEnd = cursor_positon;
});


clear_button.click(function() {
    localStorage.setItem("savedText", "");
    textbox.val("");
    textbox.focus();
});


copy_button.click(function() {
    textbox.select();
    try {
        document.execCommand("copy");
    } catch (err) {
        alert(err)
    }
    textbox.blur();
});


// Arrow buttons, changes the current font

function leftArrowAction() {
    if (current_font == 0) {
        current_font = fonts_array.length - 1;
        changeHeader();
        //textbox.focus();
        return;
    }

    current_font--;
    changeHeader();
    //textbox.focus();
}

function rightArrowAction() {
    if (current_font + 1 == fonts_array.length) {
        current_font = 0;
        changeHeader();
        //textbox.focus();
        return;
    }

    current_font++;
    changeHeader();
    //textbox.focus();
}


left_arrow.click(function() {
    leftArrowAction();
});


right_arrow.click(function() {
    rightArrowAction();
});


// The blured part checks if the user is currently in the text box or not
// so you are able to use the arrow keys in the text box
$(document).keydown(function(key) {    
    if (key.code == "ArrowLeft" && textbox_blured) {
        leftArrowAction();
    } else if (key.code == "ArrowRight" && textbox_blured) {
        rightArrowAction();
    }
});


$(document).ready(function() {

    // Saves progress
    const savedText = localStorage.getItem("savedText");
    if (savedText) {
        textbox.val(savedText);
    }


    // For hotkeys
    textbox.blur(function() {
        textbox_blured = true;
    })
    
    textbox.focus(function() {
        textbox_blured = false;
    })
});
